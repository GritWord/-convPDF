<template>
  <h1 class="cours-titre">Qu’est-ce que la blockchain ?</h1>
  <h3 class="cours-soustitre">Définition et explication</h3>
  <p class="cours-paragraphe">
    La blockchain est une technologie de stockage et de transmission
    d’informations, transparente, sécurisée, et fonctionnant sans organe
    central de contrôle (définition de Blockchain France). Elle est la
    technologie au coeur du Web Decentralisé et de son corollaire, la
    finance décentralisée. Par extension, une blockchain constitue une
    base de données qui contient l’historique de tous les échanges
    effectués entre ses utilisateurs depuis sa création. Cette base de
    données est sécurisée et distribuée : elle est partagée par ses
    différents utilisateurs, sans intermédiaire, ce qui permet à chacun de
    vérifier la validité de la chaîne. Il existe des blockchains
    publiques, ouvertes à tous, et des blockchains privées, dont l’accès
    et l’utilisation sont limitées à un certain nombre d’acteurs. Une
    blockchain publique peut donc être assimilée à un grand livre
    comptable public, anonyme et infalsifiable. Comme l’écrit le
    mathématicien Jean-Paul Delahaye, il faut s’imaginer « un très grand
    cahier, que tout le monde peut lire librement et gratuitement, sur
    lequel tout le monde peut écrire, mais qui est impossible à effacer et
    indestructible. »
  </p>
  <h3 class="cours-soustitre">Situer la blockchain</h3>
  <p class="cours-paragraphe">
    La première blockchain est apparue en 2008 avec la monnaie numérique
    bitcoin, développée par un inconnu se présentant sous le pseudonyme
    Satoshi Nakamoto. Elle en est l’architecture sous-jacente. Si
    blockchain et bitcoin ont été construits ensemble, aujourd’hui de
    nombreux acteurs (entreprises, gouvernements, etc) envisagent
    l’utilisation de la technologie blockchain pour d’autres cas que la
    monnaie numérique.
  </p>
  <h3 class="cours-soustitre">Comment ça marche ?</h3>
  <p class="cours-paragraphe">
    Toute blockchain publique fonctionne nécessairement avec une monnaie
    ou un token (jeton) programmable. Bitcoin est un exemple de monnaie
    programmable. Les transactions effectuées entre les utilisateurs du
    réseau sont regroupées par blocs. Chaque bloc est validé par les
    noeuds du réseau appelés les “mineurs”, selon des techniques qui
    dépendent du type de blockchain. Dans la blockchain du bitcoin cette
    technique est appelée le “Proof-of-Work”, preuve de travail, et
    consiste en la résolution de problèmes algorithmiques. Une fois le
    bloc validé, il est horodaté et ajouté à la chaîne de blocs. La
    transaction est alors visible pour le récepteur ainsi que l’ensemble
    du réseau Ce processus prend un certain temps selon la blockchain dont
    on parle (environ une dizaine de minutes pour bitcoin, 15 secondes
    pour Ethereum)
  </p>
